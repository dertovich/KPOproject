#include "libft.h"

/*
** LIBRARY: N/A
** DESCRIPTION:
** 		The ft_strlowcase() function converts a string str to lowercase.
*/

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
